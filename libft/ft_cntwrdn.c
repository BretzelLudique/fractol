int	ft_cntwrdn(char const *s, char c, char d, int len)
{
	int n;

	if (!s || !(*s))
		return (0);
	n = 0;
	while (*s && len)
	{
		while (len && (*s == c || *s == d))
		{
			s++;
			len--;
		}
		if (*s != '\0' && *s != c && *s != d)
		{
			n++;
			while (len && *s && *s != c && *s != d)
			{
				s++;
				len--;
			}
		}
	}
	return (n);
}
