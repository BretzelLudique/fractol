int	ft_cntwrd2(char const *s, char c, char d)
{
	int n;

	if (!s || !(*s))
		return (0);
	n = 0;
	while (*s)
	{
		while (*s == c || *s == d)
			s++;
		if (*s != '\0' && *s != c && *s != d)
		{
			n++;
			while (*s && *s != c && *s != d)
				s++;
		}
	}
	return (n);
}
