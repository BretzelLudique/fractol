#include "libft.h"

void	ft_putnbrb(const char *s, int n)
{
	ft_putstr(s);
	ft_putstr(" = ");
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + 48);
	ft_putchar('\n');
}
